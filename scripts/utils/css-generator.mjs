import { mkdirp } from 'mkdirp';
import { writeFile } from 'node:fs/promises';
import path from 'node:path';

const CATEGORY_LABELS = {
  color: 'Colors',
  typography: 'Typography',
  effect: 'Effects',
};

function propToCSS(prop) {
  return prop.replace(/[A-Z]/g, m => '-' + m.toLowerCase());
}

export async function generateCSS(tokens, outputPath, config) {
  const lines = [];
  const utilClasses = [];

  lines.push('/* ============================================================');
  lines.push('   Design Tokens - Generated by sync-figma.mjs');
  lines.push(`   Source: Figma file ${config.fileKey}`);
  lines.push(`   Generated: ${new Date().toISOString()}`);
  lines.push('   DO NOT EDIT THIS FILE - it will be overwritten on next sync');
  lines.push('   ============================================================ */');
  lines.push('');
  lines.push(':root {');

  let currentCategory = '';
  for (const token of tokens) {
    if (token.category !== currentCategory) {
      currentCategory = token.category;
      const label = CATEGORY_LABELS[currentCategory] || currentCategory;
      lines.push(`  /* ${'─'.repeat(2)} ${label} ${'─'.repeat(54 - label.length)} */`);
    }

    if (token.category === 'color' || token.category === 'effect') {
      lines.push(`  ${token.cssVar}: ${token.value};`);
    } else if (token.category === 'typography') {
      for (const [prop, val] of Object.entries(token.properties)) {
        const cssProp = propToCSS(prop);
        lines.push(`  ${token.cssVarPrefix}-${cssProp}: ${val};`);
      }
    }
  }

  lines.push('}');
  lines.push('');

  // Generate typography utility classes
  const textTokens = tokens.filter(t => t.category === 'typography');
  if (textTokens.length > 0) {
    lines.push('/* Typography utility classes (auto-generated) */');
    for (const t of textTokens) {
      const className = `figma-text-${t.name}`;
      utilClasses.push(`.${className} {`);
      for (const [prop, val] of Object.entries(t.properties)) {
        const cssProp = propToCSS(prop);
        utilClasses.push(`  ${cssProp}: var(${t.cssVarPrefix}-${cssProp});`);
      }
      utilClasses.push('}');
      utilClasses.push('');
    }
  }

  const fullPath = path.resolve(outputPath);
  await mkdirp(path.dirname(fullPath));
  await writeFile(fullPath, lines.join('\n') + '\n' + utilClasses.join('\n') + '\n', 'utf-8');
  console.log(`Generated ${outputPath} (${tokens.length} tokens)`);
}
